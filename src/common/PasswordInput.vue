<template>
  <div>
    <el-input placeholder="密码" :type="showPassword ? 'text' : 'password'" v-model="password" ref="password"></el-input>
    <i class="el-icon-view show-password" :class="{'show-password-active': showPassword}" @click="toggleShowPassword"></i>
  </div>
</template>

<script>
  export default {
    props: ['prop', 'value'],
    data () {
      return {
        showPassword: false
      }
    },
    methods: {
      toggleShowPassword () {
        this.showPassword = !this.showPassword
      },
      focus () {
        this.$refs.password.focus()
      }
    },
    computed: {
      password: {
        get () {
          return this.value
        },
        set (val) {
          this.$emit('input', val)
        }
      }
    }
  }
</script>

